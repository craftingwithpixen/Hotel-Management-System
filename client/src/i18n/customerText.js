export const getCustomerText = (lang = 'en') => {
  const active = {
    ...dictionaries.en,
    ...sectionText.en,
    ...(dictionaries[lang] || {}),
    ...(sectionText[lang] || {}),
  };
  return (key) => active[key] || key;
};

export const languageName = (lang = 'en') => {
  const t = getCustomerText(lang);
  return {
    en: t('english'),
    hi: t('hindi'),
    mr: t('marathi'),
  }[lang] || lang;
};
